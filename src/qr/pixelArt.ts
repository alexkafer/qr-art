/**
 * Convert a PixelArt placed at a position on the QR grid into ArtPixel array.
 */
export function artToPixels(
  art: PixelArt,
  startRow: number,
  startCol: number
): { row: number; col: number; value: 0 | 1 }[] {
  const pixels: { row: number; col: number; value: 0 | 1 }[] = [];
  for (let r = 0; r < art.height; r++) {
    for (let c = 0; c < art.width; c++) {
      pixels.push({
        row: startRow + r,
        col: startCol + c,
        value: art.grid[r][c] as 0 | 1,
      });
    }
  }
  return pixels;
}
