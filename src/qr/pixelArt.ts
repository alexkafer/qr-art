/**
 * Look up a glyph for a character. Supports A-Z, 0-9, symbols, and special characters.
 */
function getGlyph(char: string): number[][] | null {
  const upper = char.toUpperCase();
  if (FONT_5x7[upper]) return FONT_5x7[upper];
  if (SYMBOL_GLYPHS[char]) return SYMBOL_GLYPHS[char];
  return null;
}

/**
 * Compose multiple character grids into a single pixel art with 1-module gaps.
 */
function composeText(chars: number[][][]): PixelArt {
  const charWidth = 5;
  const charHeight = 7;
  const gap = 1;
  const totalWidth = chars.length * charWidth + (chars.length - 1) * gap;

  const grid: number[][] = [];
  for (let row = 0; row < charHeight; row++) {
    const rowData: number[] = [];
    for (let ci = 0; ci < chars.length; ci++) {
      for (let col = 0; col < charWidth; col++) {
        rowData.push(chars[ci][row][col]);
      }
      if (ci < chars.length - 1) {
        rowData.push(0); // gap
      }
    }
    grid.push(rowData);
  }

  return { name: 'composed', width: totalWidth, height: charHeight, grid };
}

/**
 * Convert a text string to pixel art using the built-in 5×7 font.
 * Unsupported characters are replaced with a blank space.
 */
export function textToPixelArt(text: string): PixelArt | null {
  if (!text || text.length === 0) return null;

  const glyphs: number[][][] = [];
  // Iterate through the string, handling multi-byte characters
  for (const char of text) {
    const glyph = getGlyph(char);
    glyphs.push(glyph ?? FONT_5x7[' ']);
  }

  if (glyphs.length === 0) return null;
  return composeText(glyphs);
}

